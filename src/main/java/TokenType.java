/**
 * Token类型
 */
public enum TokenType {
    // 关键字
    PROGRAM, VAR, CONST, BEGIN, END, WHILE, DO, IF, THEN,

    // 标识符
    IDENT,

    // 整数
    NUMBER,

    // 算符
    ADD, MINUS, MUL, DIVIDE, ASSIGN, EQUAL, NOT_EQUAL, LESS, LESS_EQUAL, GREATER, GREATER_EQUAL,

    // 界符
    LPAREN, RPAREN, SEMICOLON, COMMA,



}
